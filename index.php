                                    				<p><h5>General principles governing leave with relevant Central Civil Service (Conduct) rules, 1964 provisions</h5><br />
Leave is a provision given to an employee to stay away from work for genuine reasons with prior approval of the competent authority. It may be granted for a casual purpose or a planned activity or on medical ground or in extra ordinary conditions.</p><p><span style="font-family:georgia,serif;"><u>General provisions as per Central Civil Service (Conduct), 1964 rules</u>:</p>
<p><br />
<span style="font-family:georgia,serif;">1.1 Leave can not be claimed as a matter of right &ndash; Rule 7 (1).</span></p>
<p> 1.2 The leave sanctioning authority may refuse or revoke leave of any kind, but cannot alter the kind of leave due and applied for &ndash; Rule 7</p>
<p>1.3 Leave of one kind taken earlier may be converted into leave of a different kind at a later date at the request of the official and at the discretion of authority who granted the leave. The employee should apply for such conversion within 30 days of completion of the relevant spell of leave. This, however, cannot be claimed as a matter of right</p>
<p>1.4 Conversion of one kind of leave into a leave of a different kind is permissible only when applied for by the official while in service and not after quitting service &ndash; Rule 10 GID.</p>
<p>1.5 The leave sanctioning authority may commute retrospectively periods of absence without leave into Extra Ordinary Leave &ndash; Rule 32 (6). </p>
<p>1.6 No leave of any kind can be granted for a continuous period exceeding 5 years except with the sanction of the President. Without a valid reason, a Government servant who remains absent for continuous period exceeding five years other than foreign seervice, with or without leave, should be deemed to have resigned from Government service &ndash; Rule 12.</p>
<p>1.7 An official on leave should not take up any service or employment elsewhere without obtaining prior sanction of the competent authority &ndash; Rule 13. </p>
<p>1.8 Wilful absence from duty after the expiry of leave renders an employee liable to disciplinary action &ndash; Rule 25 (2).</p>
<p>1.9 Absence without leave not in continuation of any authorized leave will constitute an interruption of service unless it is regularised audit instruction under Rule 25 (1).</p>
<p>1.10 Leave will not be granted to an employee under suspension.</p>
<p>1.11&nbsp; Leave should always be applied for and sanctioned before it is taken, except in cases of emergency and for satisfactory reasons. </p>

<h5>&nbsp; Applicability</h5>
<p>2.These leave rules shall be applicable to all the permanent employees of this Institute with effect from 01.01.2019, as far as Earned Leave (EL) and Half Pay Leave (HPL) are concerned. Other kinds of leave will be effective from the date as approved by the Board. 2.3. Leave Sanctioning Authority</p>
<br />
<p>3.1 Applications for leave, except casual leave shall be addressed to the Registrar by the employees of the Institute</p>
<p>3.2 Leave may be sanctioned by the Director or by a member of staff to whom the power has been delegated by the Director. Normally, the Registrar will regulate the leave accounts of the employees of the Institute except casual leave.</p>
<p>3.3 In case of casual leave, the Head of the Department/ Centre/ Section will be the sanctioning authority for all the employees working in the Department/ Centre/ Section and Registrar will be the sanctioning authority for all other employees. Casual leave account of all the employees working in a Department/ Centre/ Section is to be maintained by the Head of the Department/ Centre/ Section. Director is the sanctioning authority for casual leave to the Registrar &amp; Deans. </p>

<h5><br />&nbsp;
	Start of leave and termination of leave</h5>
<p><br />
4. Leave ordinarily begins from the date on which leave as such is actually availed and ends on the day preceding the date on which duty is resumed. 2.4.2 Saturdays, Sundays and other holidays or vacation, if any, may be prefixed as well as suffixed to any leave, subject to any limit of absence on leave prescribed under each category of leave.</p>
<h5><br />&nbsp;
	Combination of leave</h5>
<p><br />
5. Except as otherwise provided, any kind of leave under these guidelines may be granted in combination with or in continuation of any other kind of leave, subject to any limits prescribed in such cases and as per the prescribed norms of Central Civil Service(Conduct) Rules, 1964 with certain exceptions.</p>
<h5><br />&nbsp;Combination of holidays with leave</h5>
<p><br />
6. When the day, immediately preceding the day on which an employee&rsquo;s leave (other than leave on medical certificate) begins or immediately following the day on which his/her leave expires, is a holiday or one of series of holidays, the employee shall be deemed to have been permitted to leave the Institute at the close of the day before, or return to it on the day following such holiday or series of holidays provided that such actions does not affect the normal working of the Institute. 2.6.2 In case of leave on medical certificate, (a) When the employee is certified medically unwell to attend office, holiday(s) if any, immediately preceeding the day he/she is so certified shall be allowed automatically to be prefixed to leave and the holiday(s) if any, immediately succeeding the day he/she is so certified (including that day) shall be treated as part of the leave. (b) When the employee is certified medically fit for joining duty, holiday(s), if any, succeeding the day he is so certified (including that day) shall automatically be allowed to be suffixed to the leave and holiday(s) preceding the day he is so certified shall be treated as part of the leave. </p><p>&nbsp;</p>
<h5>
	Grant of leave on resignation / date of retirement</h5>
<p><br />
No leave shall be granted beyond the date on which an employee must compulsorily retire. If an employee of the Institute resigns; he shall not be granted either prior or subsequent to his resignation, any leave due to his credit. Provided that the Director may, in any case, grant leave to an employee prior to his resignation, if in the opinion of the Director, the circumstances of the case justify such grant of leave.</p>
<h5></br>
	Reporting duty before the expiry of leave</h5>
<p><br />
Except with the permission of the authority, who granted leave, no employee on leave may report for duty before the expiry of the period of leave granted to him. 2.9 Reporting duty on return from leave on medical grounds<br />
An employee who has been granted leave on medical certificate is required to submit a medical certificate of fitness before resuming duty. Leave sanctioning authority may secure second medical opinion, if considered necessary.</p>
<h5></br>
	Maximum period of absence</h5>
<p><br />
An employee who remains absent from duty for a continuous period exceeding 5 years other than on foreign service or other service approved by MHRD, whether with or without leave, shall be deemed to have resigned from the service of the Institute</p>

